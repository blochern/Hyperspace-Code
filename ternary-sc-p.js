// switch case

var numberOfBabes = 3;

switch (numberOfBabes) {
  case 1:
    console.log("You got one babe");
    break;
  case 2:
    console.log("You got two babes");
    break;
  case 3:
    console.log("You got tres bebes");
    break;
  case 4:
    console.log("You got a quad of babes");
    break;
  case 5:
    console.log("You got five babes!");
    break;
  case 6:
    console.log("That's too many babes m8");
}